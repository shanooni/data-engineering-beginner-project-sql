-- coding order of select query
-- SELECT
-- DISTINCT
-- columns
-- FROM table_name
-- WHERE condition
-- GROUP BY column_name
-- HAVING condition
-- ORDER BY column_name ASC|DESC
-- LIMIT number;


-- Execution Order of SELECT Query
-- FROM clause
-- WHERE clause -> filter records based on a specified condition
-- GROUP BY clause -> group records based on a specified column
-- HAVING clause -> filter grouped records based on a specified condition
-- SELECT DISTINCT clause -> return only distinct (different) values
-- ORDER BY clause -> sort the result set in ascending or descending order
-- LIMIT clause -> restrict the number of records to return

